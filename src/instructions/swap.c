#include "../../inc/push_swap.h"

void	swap(t_list **a)
{
	t_list	*tmp;

	if (!(*a) || !((*a)->next))
		return ;
	tmp = (*a)->next;
	(*a)->next = tmp->next;
	tmp->next = (*a);
	(*a) = tmp;
}
